#include <stdio.h>

/**
 * add - this add 2 integers
 * @a: first integer
 * @b: second integer
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - this subtract 2 integers
 * @a: first integer
 * @b: second integer
 * Return: subtract final results
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - this modules 2 integers
 * @a: first integer
 * @b: second integer
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - this multiplies 2 integers
 * @a: first integer
 * @b: second integer
 * Return: multiplies final results
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - this divides 2 integers
 * @a: first integer
 * @b: second integer
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
